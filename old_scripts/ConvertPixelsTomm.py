deltax = 0.007972665 #mm/pixel
deltay = 0.007575757 #mm/pixel

#returns list of x, y arrays
def convertpixtomm(xarray, yarray):
    xarraymm = xarray*deltax
    yarraymm = yarray*deltay
    return [xarraymm, yarraymm]

